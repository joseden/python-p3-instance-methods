#!/usr/bin/env python3
class Person:
    
    def talk(self):
        print("Hello World!")

    def walk(self):
        print("The person is walking.")

# Creating instances of the Person class
guy = Person()

#Calling the talk method for each person
guy.talk()    # Output: Hello World!

# Calling the walk method for each person
guy.walk()    # Output: The person is walking.

