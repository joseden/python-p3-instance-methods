#!/usr/bin/env python3

class Dog:
    
    def bark(self):
        print("Woof!")

    def sit(self):
        print("The dog is sitting.")

# Creating instances of the Dog class
fido = Dog()
snoopy = Dog()
lassie = Dog()

# Calling the bark method for each dog
fido.bark()   # Output: Woof!
snoopy.bark() # Output: Woof!
lassie.bark() # Output: Woof!

# Calling the sit method for each dog
# The sit method will print "The dog is sitting" for each instance
fido.sit()    # Output: The dog is sitting
snoopy.sit()  # Output: The dog is sitting
lassie.sit()  # Output: The dog is sitting


